#include <stdio.h>
/**
 * add - functions adds 2 numbers
 * @a: first number
 * @b: second number
 * Return: return sum of both numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function subtracts number from another
 * @a: first number
 * @b: second number
 * Return: return result of subttraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: return result of multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function for division
 * @a: first number
 * @b: second number
 * Return: return result of division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function for modulos
 * @a: first number
 * @b: srcond number
 * Return: return modulos of 2 numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
